const DICTIONARIES: Record<Language, Dictionary> = {
  "zh-CN": zhCN,
  en: enUS,
};

export type TranslationKey = keyof typeof zhCN;

export function translate(language: Language, key: TranslationKey | string): string {
  const dict = DICTIONARIES[language] ?? enUS;
  if (key in dict) return dict[key];
  if (key in enUS) return enUS[key as TranslationKey];
  return String(key);
}

export function dictionary(language: Language): Dictionary {
  return DICTIONARIES[language] ?? enUS;
}

export function translationKeys(): readonly string[] {
  return Object.keys(zhCN);
}

/**
 * Localize common status / mode / policy tokens that come back from the API as
 * lower-case snake_case strings. Falls back to a humanized form of the token
 * when no mapping exists.
 */
export function translateDynamic(language: Language, raw: string | null | undefined): string {
  if (!raw) return "";
  const key = `dyn.${raw}`;
  if (key in DICTIONARIES[language]) return DICTIONARIES[language][key];
  if (key in enUS) return enUS[key];
  // Humanize fallback (e.g. "mock_safe_eval" -> "mock safe eval").
  return String(raw).replaceAll("_", " ").replaceAll("-", " ");
}
